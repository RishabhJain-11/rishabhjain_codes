# prompt: import all the basic libraries along with SVM

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn.svm import SVC
from sklearn.metrics import accuracy_score
from sklearn import svm

# prompt: read the dataset and read forst 5 lines

# Assuming the dataset is stored in a file named 'dataset.csv'
data = pd.read_csv('loan_dataset.csv')
data.head()

data.shape

data.describe()

data.isnull().sum()

data = data.dropna()

data.isnull().sum()

data.replace({"Loan Status : ": {'N': 0, 'Y': 1}}, inplace = True)

data['Dependents'].value_counts()

data = data.replace(to_replace = '3+', value = 4)

data['Dependents'].value_counts()

sns.countplot(x = "Education", hue = "Loan_Status", data = data)

sns.countplot(x = "Married", hue = "Loan_Status", data = data)

data.replace({'Married':{'No':0,'Yes':1},'Gender':{'Male':1,'Female':0},'Self_Employed':{'No':0,'Yes':1},
                      'Property_Area':{'Rural':0,'Semiurban':1,'Urban':2},'Education':{'Graduate':1,'Not Graduate':0}},inplace=True)

data.head()

X = data.drop(columns=['Loan_ID','Loan_Status'],axis=1)
Y = data['Loan_Status']

print(X)
print(Y)

X_train, X_test,Y_train,Y_test = train_test_split(X,Y,test_size=0.1,stratify=Y,random_state=2)
print(X.shape, X_train.shape, X_test.shape)

classifier = svm.SVC(kernel='linear')
classifier.fit(X_train,Y_train)

X_train_prediction = classifier.predict(X_train)
training_data_accuray = accuracy_score(X_train_prediction,Y_train)
print('Accuracy on training data : ', training_data_accuray)

X_test_prediction = classifier.predict(X_test)
test_data_accuray = accuracy_score(X_test_prediction,Y_test)
print('Accuracy on test data : ', test_data_accuray)

